<h2 id="by-taxi">By Taxi</h2>
<p>
	Taxis are ubiquitous in Ho Chi Minh City, offering a comfortable and convenient mode of
	transportation to the Ly Thuong Kiet Campus. Numerous taxi companies operate in the city, with
	fares ranging from 14,000-20,000 Vietnamese Dong (VND) for a short trip.
</p>
<h2 id="by-motorbike">By Motorbike</h2>
<p>
	Motorbikes are a popular mode of transportation in Vietnam, and the Ly Thuong Kiet Campus has a
	designated motorbike parking lot. If you have your own motorbike, you can easily park it at the
	lot and walk to your destination.
</p>
