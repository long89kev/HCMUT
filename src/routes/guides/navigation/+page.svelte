<h1 id="navigation">Navigation</h1>
<p>
	Once you&#39;re on campus, navigating the Ly Thuong Kiet Campus is straightforward thanks to the
	well-organized layout and signage. Here are some helpful tips:<br />
</p>
<ul>
	<li>
		<p>
			<strong>Install the <abbr title="Ho Chi Minh City University of Technology">HCMUT</abbr> Guides app:</strong> The app provides a detailed map of the campus,
			including building locations, parking lots, and other facilities.
		</p>
	</li>
	<li>
		<p>
			<strong>Utilize Campus Signage:</strong> Clear and informative signage is strategically placed
			throughout the campus, guiding you to specific buildings, classrooms, and other locations.
		</p>
	</li>
	<li>
		<p>
			<strong>Embrace Information Desks:</strong> Information desks are located in the main lobby of
			each building and are staffed by helpful personnel who can provide directions and assistance.
		</p>
	</li>
</ul>
<h2>Specific Facilities</h2>
<ul>
	<li>
